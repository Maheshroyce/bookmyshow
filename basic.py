# Type conversion in Python (basic examples)

# int to float
a = 5
b = float(a)
print(b)

# float to int
c = 3.7
d = int(c)
print(d)

# int to string
e = 10
f = str(e)
print(f)

# string to int
g = "25"
h = int(g)
print(h)

# string to float
i = "12.34"
j = float(i)
print(j)

# float to string
k = 45.67
l = str(k)
print(l)
